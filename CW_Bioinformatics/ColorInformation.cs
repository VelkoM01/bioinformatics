namespace CW_Bioinformatics
{
    class ColorInformation
    {
        public int AverageRed { get; }
        public int AverageGreen { get; }
        public int AverageBlue { get; }

        public ColorInformation(int averageRed, int averageGreen, int averageBlue)
        {
            AverageRed = averageRed;
            AverageGreen = averageGreen;
            AverageBlue = averageBlue;
        }
    }
}


